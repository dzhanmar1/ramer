interface I18nState {
  lang: Lang;
  setLang: (lang: Lang) => void;
  t: (key: string) => string;
}

export const useI18nStore = create<I18nState>()(
  persist(
    (set, get) => ({
      lang: 'ru',
      setLang: (lang) => set({ lang }),
      t: (key) => {
        const { lang } = get();
        // @ts-ignore
        return translations[lang][key] || key;
      }
    }),
    {
      name: 'i18n-storage'
    }
  )
);
